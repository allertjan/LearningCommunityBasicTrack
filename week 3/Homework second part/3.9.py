import turtle
window = turtle.Screen()
tess = turtle.Turtle()
tess.right(90)
# turn 90 degrees to
tess.left(3600) # turn 3600 degrees to left which will be the same place as where tess started.
tess.right(-90) # turn 90 degrees to the left
tess.speed(10) # set the speed of tess to 10
tess.left(3600) # see above
tess.speed(0) # set speed tess to 0
tess.left(3645) # Turn 3645 degrees to the left which is the same as 45 degrees to the left
tess.forward(-100) # Move tess 100 back